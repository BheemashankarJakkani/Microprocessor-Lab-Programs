.MODEL SMALL
.DATA
 ARR DW 5000H,2000H,1000H,8000H,7000H
 KEY DW 2500H
 LEN EQU ($-ARR)/2
 M1 DB 10,13,"THE ELEMENT FOUND AT $"
 M2 DB 10,13,"THE ELEMENT NOT FOUND$"

.CODE
 MOV AX,@DATA
 MOV DS,AX
 MOV BX,01
 MOV DX,LEN
 MOV CX,KEY

AGAIN:CMP BX,DX
      JA FAIL
      MOV AX,BX
      ADD AX,DX
      SHR AX,01
      MOV SI,AX
      MOV DI,SI
      DEC SI
      ADD SI,SI
      CMP CX,ARR[SI]
      JAE BIG
      DEC AX
      MOV DX,AX
      JMP AGAIN
BIG:JE SUCCESS
    INC AX
    MOV BX,AX
    JMP AGAIN

SUCCESS:LEA DX,M1
        MOV AH,09H
        INT 21H

        ADD DI,3030H
        MOV DX,DI
        MOV AH,02H
        INT 21H
        JMP TERMINATE

FAIL:LEA DX,M2
     MOV AH,09H
     INT 21H
     MOV AH,4CH
     INT 21H

TERMINATE:MOV AH,4CH
           INT 21H

           END
