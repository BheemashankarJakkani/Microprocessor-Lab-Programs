.MODEL SMALL
.DATA
ARR DW 5558H,4447H,7885H,8863H
CNT EQU ($-ARR)/2

.CODE
MOV AX,@DATA
MOV DS,AX
MOV DL,CNT-1

L1:MOV CL,DL
   LEA SI,ARR
L2:MOV AX,[SI]
   CMP AX,[SI+2]
   JC L3
   XCHG [SI+2],AX
   XCHG [SI],AX

L3:INC SI
   INC SI
   LOOP L2
   DEC DL
   JNZ L1

  INT 3H
  ALIGN 16
  END

