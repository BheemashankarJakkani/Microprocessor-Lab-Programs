.MODEL SMALL
.DATA
 STR DB "MALAYALAM$"
 LEN EQU 9
 M1 DB 10,13,"IT IS PALINDROME$"
 M2 DB 10,13,"IT IS NOT PALINDROME$"
 REVSTR DB 10 DUP(?)

.CODE
 MOV AX,@DATA
 MOV DS,AX
 MOV ES,AX

 LEA SI,STR
 LEA DI,REVSTR
 MOV CX,LEN
 ADD SI,CX
 DEC SI

AGAIN:MOV AL,[SI]
      MOV [DI],AL
      DEC SI
      INC DI
      LOOP AGAIN

      MOV CX,LEN
      LEA SI,STR
      LEA DI,REVSTR
      CLD
      REPE CMPSB
      JCXZ MATCH
      LEA DX,M2
      MOV AH,09H
      INT 21H
      JMP TERMINATE
MATCH:LEA DX,M1
      MOV AH,09H
      INT 21H      
      JMP TERMINATE
TERMINATE:MOV AH,4CH
          INT 21H
          END
